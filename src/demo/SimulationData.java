	public int anyDeliveriesToday(int day) {
		int deliveries = 0;
		for (int index = 0; index < delivery.size(); index++) {
			VarehouseDelivery deliveryItem = delivery.get(index);
			if (deliveryItem.deliveryDay == day) {
				log.debug("Got delivery by " + deliveryItem.deliveryMode + ": " + deliveryItem.numberOfDrums);
				deliveries += deliveryItem.numberOfDrums;
				delivery.remove(index);
			}
		}
		return deliveries;
	}
	
